import { Type } from 'class-transformer';
import {
  IsArray,
  IsEnum,
  IsNumber,
  IsOptional,
  IsString,
  ValidateNested,
} from 'class-validator';
import { TopLevelCategory } from '../schemas/top-page.schema';

export class HhData {
  @IsNumber()
  count: number;

  @IsNumber()
  juniorSalary: number;

  @IsNumber()
  middleSalary: number;

  @IsNumber()
  seniorSalary: number;
}

export class TopAdvantage {
  @IsString()
  title: string;

  @IsString()
  description: string;
}

export class createTopPageDto {
  @IsEnum(TopLevelCategory)
  firstCategory: TopLevelCategory;

  @IsString()
  secondCategory: string;

  @IsString()
  title: string;

  @IsString()
  category: string;

  @IsOptional()
  @ValidateNested()
  @Type(() => HhData)
  hh?: HhData;

  @IsArray()
  @ValidateNested()
  @Type(() => TopAdvantage)
  advantages: TopAdvantage[];

  @IsString()
  seoText: string;

  @IsArray()
  @IsString({ each: true })
  tags: string[];

  @IsString()
  tagsTitle: string;
}
